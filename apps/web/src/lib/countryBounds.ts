// Common aliases the analysis pipeline may output instead of the canonical name
const ALIASES: Record<string, string> = {
  'USA':         'United States',
  'US':          'United States',
  'U.S.A.':      'United States',
  'UK':          'United Kingdom',
  'U.K.':        'United Kingdom',
  'Korea':       'South Korea',
  'UAE':         'United Arab Emirates',
}

export function getCountryBounds(country: string): BBox | null {
  const canonical = ALIASES[country] ?? country
  return COUNTRY_BOUNDS[canonical] ?? null
}
